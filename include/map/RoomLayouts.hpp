struct RoomVariants {
    std::vector<VECTOR2D> variants;

    void addVariant(VECTOR2D& variant) {
        variants.push_back(variant);
    }

    VECTOR2D getVariantData(int index) {
        return variants[index];
    }

    int getVariantsAmount() {
        return variants.size();
    }
};

